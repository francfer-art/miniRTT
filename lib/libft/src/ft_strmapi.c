#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*p;
	int		i;

	i = 0;
	if (!s || !f)
	{
		return (0);
	}
	p = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (p == 0)
	{
		return (0);
	}
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
